    private void btnRegister2ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnRegister2ActionPerformed
    {//GEN-HEADEREND:event_btnRegister2ActionPerformed
        //check if username is taken, if not, register
        String sql = "SELECT * FROM LoginDetails WHERE Username LIKE '" + txfRegisterUser.getText() + "';";
        ResultSet rs = connect.query(sql);
        int count = 0;
        try
        {
            while (rs.next())
            {
                count++;
            }
            if (count == 1)
            {
                JOptionPane.showMessageDialog(this, "Username taken.");
            } else if (count == 0)
            {
                if (txfRegisterUser.getText().equals("") || txfRegisterPass.getText().equals(""))
                {
                    JOptionPane.showMessageDialog(this, "Enter a username and password.");
                } else if (txfRegisterUser.getText().length() < 13)
                {
                    sql = "INSERT INTO LoginDetails (Username, Password) VALUES ('"
                            + txfRegisterUser.getText() + "', '"
                            + txfRegisterPass.getText() + "');";

                    int records = connect.update(sql);

                    JOptionPane.showMessageDialog(this, records + " record inserted");
                    frmRegister.setVisible(false);
                    frmLogin.setVisible(true);
                } else if (txfRegisterUser.getText().length() >= 13)
                {
                    JOptionPane.showMessageDialog(this, "Username must not exceed 12 characters.");
                }
            }
        } catch (SQLException ex)
        {
            Logger.getLogger(HangmanGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegister2ActionPerformed

