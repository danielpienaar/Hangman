    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnInsertActionPerformed
    {//GEN-HEADEREND:event_btnInsertActionPerformed
        //validate and insert phrase into selected level
        String sql = "";
        String word = txfInsert.getText();
        String level = "";
        if (cmbInsert.getSelectedItem() == "Level 1")
        {
            level = "1";
        } else if (cmbInsert.getSelectedItem() == "Level 2")
        {
            level = "2";
        } else if (cmbInsert.getSelectedItem() == "Level 3")
        {
            level = "3";
        }
        sql = "INSERT INTO tblPhrases (Phrases, Level) VALUES ('" + word.toLowerCase() + "', '" + level + "');";
        if (word.chars().allMatch(Character::isLetter) || word.contains(" "))
        {
            int records = connect.update(sql);
            JOptionPane.showMessageDialog(this, records + " record/s inserted");
        } else if (!word.chars().allMatch(Character::isLetter))
        {
            JOptionPane.showMessageDialog(this, "Word may only contain letters and spaces.");
        }

    }//GEN-LAST:event_btnInsertActionPerformed

