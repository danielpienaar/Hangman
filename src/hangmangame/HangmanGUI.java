    private void btnActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnActionPerformed
    {//GEN-HEADEREND:event_btnActionPerformed
        //Main method play
        JButton temp = (JButton) evt.getSource();
        display = play.calculateGuess(temp.getText(), play.getWord(), display);
        lblWord.setText(" " + display);
        lblGuesses.setText(" " + "Guesses left: " + play.getGuesses());
        lblDisplayScore.setText(" " + "Score: " + play.getScore());
        int hanged = play.getGuesses();
        if (!display.contains("-"))
        {
            frmPlay.setEnabled(false);
            frmWin.setVisible(true);
            frmWin.setIconImage(stickman.getImage());
            frmWin.setLocationRelativeTo(this);
            lblScore.setText("Score: " + play.getScore());
            lblGoodJob.setText("Good job " + play.getUsername() + "!");
        } else if (display.contains("-"))
        {
            switch (hanged)
            {
                case 5:
                    lblHead.setIcon(head);
                    break;
                case 4:
                    lblBody.setIcon(body);
                    break;
                case 3:
                    lblArm1.setIcon(arm1);
                    break;
                case 2:
                    lblArm2.setIcon(arm2);
                    break;
                case 1:
                    lblLeg1.setIcon(leg1);
                    break;
                case 0:
                    lblLeg2.setIcon(leg2);
                    frmPlay.setEnabled(false);
                    frmLose.setVisible(true);
                    frmLose.setIconImage(stickman.getImage());
                    frmLose.setLocationRelativeTo(this);
                    lblFailedWord.setText(play.getWord());
            }
        }
        temp.setEnabled(false);
    }//GEN-LAST:event_btnActionPerformed

